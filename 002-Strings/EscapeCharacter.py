# Escape Characters

# To insert characters that are illegal in a string, use
# an escape character
# This is a backslash \ followed by the character you want to
# insert

# An example of an illegal character is a double quote
# inside a string that is surrounded by double quotes

# E.g.
# txt = "We don't take kindly to "YOUR" types around here!"

# To fix this:

txt = "We don't take kindly to \"YOUR\" types around here!"
print(txt)

