# Joining Tuples

# To join two or more tuples, use the + operator

# E.g. Join two tuples
tuple1 = ("a", "b", "c")
tuple2 = (1, 2, 3)

tuple3 = tuple1 + tuple2
print(tuple3)

# Multiply Tuples
# To multiple the content of a tuple n number of times, use the * operator

fruits = ("apple", "banana", "cherry")
myTuple = fruits * 2
print(myTuple)
